"use client";

import { useEffect, useState } from "react";
import RoomCard from "@/components/home/RoomCard";
import { getRooms } from "@/lib/roomService";

export default function SearchPage() {
  const [rooms, setRooms] = useState<any[]>([]);
  const [filteredRooms, setFilteredRooms] = useState<any[]>([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  // C√°c tr∆∞·ªùng t√¨m ki·∫øm
  const [searchTerm, setSearchTerm] = useState("");
  const [bedroom, setBedroom] = useState("");
  const [bed, setBed] = useState("");

  // G·ªçi API l·∫•y danh s√°ch ph√≤ng t·ª´ Cybersoft
  useEffect(() => {
    const fetchRooms = async () => {
      try {
        setLoading(true);
        setError(null);

        const result = (await getRooms()) as {
          success: boolean;
          rooms: any[];
          message?: string;
        };

        if (result.success) {
          console.log("ƒê√£ l·∫•y ƒë∆∞·ª£c ph√≤ng:", result.rooms);
          setRooms(result.rooms);
          setFilteredRooms(result.rooms);
        } else {
          setError(result.message || "Kh√¥ng th·ªÉ l·∫•y d·ªØ li·ªáu ph√≤ng");
        }
      } catch (err: any) {
        console.error("L·ªói fetch rooms:", err);
        setError(err.message || "C√≥ l·ªói x·∫£y ra khi t·∫£i d·ªØ li·ªáu");
      } finally {
        setLoading(false);
      }
    };
    fetchRooms();
  }, []);

  // H√†m l·ªçc d·ªØ li·ªáu
  useEffect(() => {
    let result = rooms;

    if (searchTerm) {
      result = result.filter((r) =>
        r.tenPhong.toLowerCase().includes(searchTerm.toLowerCase())
      );
    }

    if (bedroom) {
      result = result.filter((r) => r.phongNgu === Number(bedroom));
    }

    if (bed) {
      result = result.filter((r) => r.giuong === Number(bed));
    }

    setFilteredRooms(result);
  }, [searchTerm, bedroom, bed, rooms]);

  return (
    <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-10">
      {/* Thanh t√¨m ki·∫øm */}
      <div className="bg-white dark:bg-gray-800 p-6 rounded-xl shadow-md mb-10">
        <h2 className="text-2xl font-bold text-gray-900 dark:text-white mb-4 text-center">
          üîç T√¨m ki·∫øm ph√≤ng
        </h2>

        <div className="grid grid-cols-1 sm:grid-cols-3 gap-4">
          {/* T√™n ph√≤ng */}
          <input
            type="text"
            placeholder="Nh·∫≠p t√™n ph√≤ng..."
            value={searchTerm}
            onChange={(e) => setSearchTerm(e.target.value)}
            className="p-3 border rounded-lg dark:bg-gray-700 dark:text-white focus:ring-2 focus:ring-blue-500 outline-none"
          />

          {/* S·ªë ph√≤ng ng·ªß */}
          <input
            type="number"
            placeholder="S·ªë ph√≤ng ng·ªß"
            value={bedroom}
            onChange={(e) => setBedroom(e.target.value)}
            className="p-3 border rounded-lg dark:bg-gray-700 dark:text-white focus:ring-2 focus:ring-blue-500 outline-none"
          />

          {/* S·ªë gi∆∞·ªùng */}
          <input
            type="number"
            placeholder="S·ªë gi∆∞·ªùng"
            value={bed}
            onChange={(e) => setBed(e.target.value)}
            className="p-3 border rounded-lg dark:bg-gray-700 dark:text-white focus:ring-2 focus:ring-blue-500 outline-none"
          />
        </div>
      </div>

      {/* Danh s√°ch ph√≤ng */}
      {loading ? (
        <p className="text-center text-gray-500">ƒêang t·∫£i d·ªØ li·ªáu...</p>
      ) : error ? (
        <p className="text-center text-red-500">L·ªói: {error}</p>
      ) : filteredRooms.length === 0 ? (
        <p className="text-center text-gray-500">Kh√¥ng t√¨m th·∫•y ph√≤ng n√†o</p>
      ) : (
        <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-8">
          {filteredRooms.map((room) => (
            <RoomCard
              key={room.id}
              id={room.id}
              title={room.tenPhong}
              location={room.moTa}
              price={room.giaTien}
              rating={4.5}
              reviews={10}
              image={room.hinhAnh}
              amenities={[
                `${room.khach} kh√°ch`,
                `${room.phongNgu} ph√≤ng ng·ªß`,
                `${room.giuong} gi∆∞·ªùng`,
              ]}
            />
          ))}
        </div>
      )}
    </div>
  );
}
